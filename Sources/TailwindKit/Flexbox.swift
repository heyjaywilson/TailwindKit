import Foundation

public enum FlexBox: String {
    case flex = "flex"
    case wrap = "flex-wrap"
    case row = "flex-row"
    case rowReverse = "flex-row-reverse"
    case column = "flex-col"
    case columnReverse = "flex-col-reverse"
    case grow = "grow"
}
